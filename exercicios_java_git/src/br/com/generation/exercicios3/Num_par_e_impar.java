package br.com.generation.exercicios3;

import java.util.Scanner;

public class Num_par_e_impar {

	public static void main(String[] args) {
		Scanner entrada=new Scanner(System.in);
		int num, cont1=0, cont2=0;
		for(int i=0;i<10;i++) {
			System.out.println("digite o numero: ");
			num=entrada.nextInt();
			if(num%2==0) {
				cont1++;
			}
			else {
				cont2++;
			}
			
		}
		System.out.println("o total de pares são: "+cont1);
		System.out.println("o total de impares são: "+cont2);
		entrada.close();
	}

}
